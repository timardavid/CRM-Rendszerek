export function OAuthButtons({ google, github }: { google: boolean; github: boolean }) {
  if (!google && !github) return null;

  return (
    <div className="flex flex-col gap-2">
      {google && (
        <Button type="button" variant="outline" className="w-full" onClick={() => signIn("google", { callbackUrl: "/" })}>
          <GoogleIcon /> Folytatás Google-lel
        </Button>
      )}
      {github && (
        <Button type="button" variant="outline" className="w-full" onClick={() => signIn("github", { callbackUrl: "/" })}>
          <GitHubIcon /> Folytatás GitHub-bal
        </Button>
      )}
      <div className="flex items-center gap-3 py-1">
        <div className="h-px flex-1 bg-border" />
        <span className="text-xs text-muted-foreground">vagy</span>
        <div className="h-px flex-1 bg-border" />
      </div>
    </div>
  );
}
